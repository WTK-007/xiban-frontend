<template>
  <div>
    <van-search
        v-model="searchText"
        show-action
        placeholder="搜索队伍"
        @search="onSearch"
        @cancel="onCancel"
    />
    <van-tabs v-model:active="active" @change="onTabChange" style="padding-bottom: 20px">
      <van-tab title="公开" name="public"></van-tab>
      <van-tab title="加密" name="private"></van-tab>
    </van-tabs>

    <van-button class="add-button" type="primary" icon="plus" @click="toAddTeam"></van-button>
    <team-card-list :teamList="teamList" />
    <van-empty v-if="teamList?.length < 1" image="search" description="未找到相关队伍" />
  </div>

</template>


<script setup lang="ts">
import {useRouter} from "vue-router";
import TeamCardList from "../components/TeamCardList.vue";
import {onMounted, ref} from "vue";
import myAxios from "../plugins/myAxios.ts";
import {showFailToast} from "vant";
import {timeFormaDetailt, timeFormat} from "../constants/team.ts";
const router = useRouter()

// Tab栏状态
const active = ref('public');

// 搜索队伍
const searchText = ref('');
// 清楚搜索框中的内容
const onCancel = () =>{
  searchText.value = '';
  listTeam();
}
const onSearch = (val) => {
  listTeam(val);
}

/**
 * 切换查询状态
 * @param name
 */
const onTabChange = (name) => {
  if (name === 'public'){
    // 查公开队伍
    listTeam(searchText.value, 0);
  } else {
    // 查加密队伍
    listTeam(searchText.value, 2)
  }

}

/**
 * 查询队伍列表
 * @param val
 * @param status
 */
const listTeam = async (val = '', status = 0) => {
  const res = await myAxios.get("/team/list", {
    params:{
      searchText: val,
      pageNum: 1,
      status,
    },
  });
  if (res && res.code === 0){
    // 对组队时间进行格式化
    const list = res.data
    for (let i = 0; i < list.length; i++) {
      const expireTime = new Date(list[i].expireTime)
      list[i].expireTime = timeFormat(expireTime)
      const createTime = new Date(list[i].createTime)
      list[i].createTime = timeFormaDetailt(createTime)
    }
    teamList.value = list
  } else {
    showFailToast("加载队伍失败，请重试")
  }
}

// 创建队伍
const toAddTeam = () => {
  router.push({
    path: "/team/add"
  })
}

const teamList = ref([]);

// (页面加载时只执行一次)查询队伍
onMounted( () => {
  listTeam()
})

</script>

